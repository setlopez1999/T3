    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        //Areggamos
        String tipo = jTextField1.getText();
        String pre = jTextField2.getText();
        if(VerificarNumero(pre)&& !(tipo.equals(""))){
            int precio = Integer.parseInt(pre);
            if(key == 1){
                Camisa nuevacamisa = new Camisa(precio);
                nuevacamisa.setTipo(tipo);
                padre.listacamisas.add(nuevacamisa);
            }
            else if(key == 2){
                Pantalon nuevap = new Pantalon(precio);
                nuevap.setTipo(tipo);
                padre.listapantalones.add(nuevap);
            }
            else if(key == 3){
                Falda nuevaf = new Falda(precio);
                nuevaf.setTipo(tipo);
                padre.listafaldas.add(nuevaf);
            }
            else if(key == 4){
                Blusa nuevab = new Blusa(precio);
                nuevab.setTipo(tipo);
                padre.listablusas.add(nuevab);
            }
            padre.ActualizarModelo();
            this.dispose();
            padre.setVisible(true);
            
        }
        
    }//GEN-LAST:event_jButton1MouseClicked

